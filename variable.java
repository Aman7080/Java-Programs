/*  Program to demonstrate different type of variable used in Java */
public class variable {
    public static void main(String[] args) {
        int num = 30; // without decimal
        float decNum = 56.89f; // decimal number
        char ch = 'a'; // single character
        String str = "This is String"; // full text
        boolean bool = true; // either true or false
        System.out.println("Num = " + num);
        System.out.println("decNum = " + decNum);
        System.out.println("ch = " + ch);
        System.out.println("str = " + str);
        System.out.println("bool = " + bool);
    }
}
